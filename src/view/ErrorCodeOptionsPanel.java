    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        db.updateErrorCodeDescription(id, (Language)languageComboBox.getSelectedItem(), descriptionTextField.getText());
        updateDescription();
        errorCodeList.setListData(db.toErrorCodeArray());
    }//GEN-LAST:event_updateButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        db.deleteErrorCodeDescription(id, ((Language)languageComboBox.getSelectedItem()).id);
        updateDescription();
        errorCodeList.setListData(db.toErrorCodeArray());
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void languageComboBoxItemStateChanged(java.awt.event.ItemEvent evt)//GEN-FIRST:event_languageComboBoxItemStateChanged
    {//GEN-HEADEREND:event_languageComboBoxItemStateChanged
        updateDescription();
        if(descriptionTextField.getText().isEmpty()) {
            addButton.setEnabled(false);
        }
    }//GEN-LAST:event_languageComboBoxItemStateChanged

    private void addButtonActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_addButtonActionPerformed
    {//GEN-HEADEREND:event_addButtonActionPerformed
        String description = descriptionTextField.getText();
        Language language = (Language)languageComboBox.getSelectedItem();
        
        db.addErrorCodeDescription(language, id, description);
        updateDescription();
    }//GEN-LAST:event_addButtonActionPerformed

    private void deleteAllButtonActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_deleteAllButtonActionPerformed
    {//GEN-HEADEREND:event_deleteAllButtonActionPerformed
        db.deleteAllErrorCodeDescriptions(id);
        updateDescription();
        errorCodeList.setListData(db.toErrorCodeArray());
    }//GEN-LAST:event_deleteAllButtonActionPerformed

    private void descriptionTextFieldKeyReleased(java.awt.event.KeyEvent evt)//GEN-FIRST:event_descriptionTextFieldKeyReleased
    {//GEN-HEADEREND:event_descriptionTextFieldKeyReleased
        if(descriptionTextField.getText().isEmpty())addButton.setEnabled(false);
        else addButton.setEnabled(true);
    }//GEN-LAST:event_descriptionTextFieldKeyReleased


