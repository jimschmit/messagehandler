    public void updateDescription(){
        if(errorCodeList != null){
            try{
                Language language = (Language)languageComboBox.getSelectedItem();
                String sampleDescription = errorCodeList.getSelectedValue().toString();
                descriptionTextField.setText(db.getErrorCodeDescription(id, language));
            }
            catch(NullPointerException e){
                
            }
        }
    }
    
    public void fillComboBox(Database db){
        ArrayList<Language> alLanguages = db.alLanguages;
        for (Language alLanguage : alLanguages)
        {
            languageComboBox.addItem(alLanguage);
        }
    }

